package com.vazjim.controlasistencias.modelo;

public class Configuracion {
	
	private String idIdentificador;
	private String identificador;
	private String valorAbajo;
	private String valorArriba;
	
	public String getIdentificador() {
		return identificador;
	}
	public void setIdentificador(String identificador) {
		this.identificador = identificador;
	}
	public String getIdIdentificador() {
		return idIdentificador;
	}
	public void setIdIdentificador(String idIdentificador) {
		this.idIdentificador = idIdentificador;
	}
	public String getValorAbajo() {
		return valorAbajo;
	}
	public void setValorAbajo(String valorAbajo) {
		this.valorAbajo = valorAbajo;
	}
	public String getValorArriba() {
		return valorArriba;
	}
	public void setValorArriba(String valorArriba) {
		this.valorArriba = valorArriba;
	}

}
